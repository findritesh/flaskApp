from flask import render_template, flash, redirect, url_for
from app import app
from app.forms import LoginForm
from datetime import datetime

@app.before_request
def before_request():
    if current_user.is_authenticated:
        current_user.last_seen = datetime.utcnow()
        db.session.commit()

